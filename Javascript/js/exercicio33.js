let valor1, valor2;
valor1 = parseInt(prompt("Digite o primeiro valor:"));
valor2 = parseInt(prompt("Digite o segundo valor:"));

if (valor1>valor2){
    alert("O primeiro é maior.");
} else
if (valor1<valor2){
    alert("O segundo é maior.");
} else
if (valor1==valor2){
    alert("Números iguais.");
}